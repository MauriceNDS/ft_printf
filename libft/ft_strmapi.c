#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		slen;
	char	*res;
	size_t	i;

	if (!s)
		return (NULL);
	slen = ft_strlen(s);
	if (!(res = malloc(sizeof(char) * (slen + 1))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
